func getVideoAspectRatio(filePath string) (string, string, error) {

	cmd := exec.Command("ffprobe", "-v", "error", "-print_format", "json", "-show_streams", filePath)

	var b bytes.Buffer
	cmd.Stdout = &b
	err := cmd.Run()
	if err != nil {
		log.Fatal(err)
		return "", "", err
	}
	fmt.Println(cmd.Stdout)

	var metadata videoMetaData
	if err := json.Unmarshal(b.Bytes(), &metadata); err != nil {
		log.Fatalf("Error unmarshalling JSON: %v", err)
		return "", "", err
	}

	width := metadata.Streams[0].Width
	height := metadata.Streams[0].Height
	aspectRatio := metadata.Streams[0].DisplayAspectRatio
	_, orientation := calculateAspectRatio(width, height)
	return aspectRatio, orientation, nil

}

func calculateAspectRatio(width int, height int) (string, string) {
	gcf := gcf(width, height)
	widthAspect := width / gcf
	heightAspect := height / gcf

	orientation := "landscape"
	if widthAspect < heightAspect {
		orientation = "portrait"
	}
	fmt.Println(widthAspect, heightAspect, orientation)
	return fmt.Sprintf("%d:%d", widthAspect, heightAspect), orientation
}

func gcf(a int, b int) int {
	for b != 0 {
		a, b = b, a%b
	}
	return a
}
